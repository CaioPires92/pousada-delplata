const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');
const { createClient } = require('@libsql/client');

function argument(name, fallback = '') {
  const prefix = `--${name}=`;
  const value = process.argv.find((item) => item.startsWith(prefix));
  return value ? value.slice(prefix.length) : fallback;
}

function sqlIdentifier(value) {
  return `"${String(value).replaceAll('"', '""')}"`;
}

function sqlValue(value) {
  if (value === null || value === undefined) return 'NULL';
  if (typeof value === 'number' || typeof value === 'bigint') return String(value);
  if (value instanceof ArrayBuffer) value = new Uint8Array(value);
  if (ArrayBuffer.isView(value)) return `X'${Buffer.from(value.buffer, value.byteOffset, value.byteLength).toString('hex')}'`;
  return `'${String(value).replaceAll("'", "''")}'`;
}

function productionCredentials(envFile) {
  const env = dotenv.parse(fs.readFileSync(envFile));
  const url = (env.DATABASE_URL || '').match(/libsql:\/\/[^\s"']+/)?.[0];
  const authToken = env.DATABASE_AUTH_TOKEN || '';
  if (!url || !authToken) throw new Error(`Credenciais Turso invalidas em ${envFile}`);
  return { url, authToken };
}

async function main() {
  const envFile = path.resolve(argument('env-file', '.env.production'));
  const output = path.resolve(argument('output'));
  if (!argument('output')) throw new Error('Informe --output=/caminho/backup.sql');
  if (fs.existsSync(output)) throw new Error(`O arquivo ja existe: ${output}`);

  fs.mkdirSync(path.dirname(output), { recursive: true });
  const client = createClient(productionCredentials(envFile));
  const transaction = await client.transaction('read');

  try {
    const schema = await transaction.execute(`
      SELECT type, name, tbl_name, sql
      FROM sqlite_master
      WHERE name NOT LIKE 'sqlite_%'
        AND sql IS NOT NULL
      ORDER BY CASE type WHEN 'table' THEN 0 WHEN 'index' THEN 1 WHEN 'trigger' THEN 2 ELSE 3 END, name
    `);
    const tables = schema.rows.filter((row) => row.type === 'table');
    const laterSchema = schema.rows.filter((row) => row.type !== 'table');
    const lines = [
      '-- Turso logical backup generated by Delplata-Motor',
      'PRAGMA foreign_keys=OFF;',
      'BEGIN TRANSACTION;',
    ];
    const rowCounts = {};

    for (const row of tables) lines.push(`${row.sql};`);

    for (const table of tables) {
      const tableName = String(table.name);
      const result = await transaction.execute(`SELECT * FROM ${sqlIdentifier(tableName)}`);
      rowCounts[tableName] = result.rows.length;
      if (result.columns.length === 0) continue;
      const columns = result.columns.map(sqlIdentifier).join(', ');
      for (const row of result.rows) {
        const values = result.columns.map((column) => sqlValue(row[column])).join(', ');
        lines.push(`INSERT INTO ${sqlIdentifier(tableName)} (${columns}) VALUES (${values});`);
      }
    }

    for (const row of laterSchema) lines.push(`${row.sql};`);
    lines.push('COMMIT;', 'PRAGMA foreign_keys=ON;', '');
    fs.writeFileSync(output, lines.join('\n'), { encoding: 'utf8', mode: 0o600, flag: 'wx' });
    console.log(JSON.stringify({ output, tables: tables.length, rowCounts }, null, 2));
  } finally {
    transaction.close();
    client.close();
  }
}

main().catch((error) => {
  console.error(`Backup failed: ${error.message}`);
  process.exit(1);
});
